console.log('Conversão de tipos');

//como usar números em formato de textos sem dar o calculo no console?
//lembrando que arrays '' são exclusivas para imprimir o texto,não calculos.
console.log( '2' + '2') //no console sai 2 com 2 (22 em formato texto)

//fazendo conversão de tipos com a função PARSEINT
console.log(parseInt('2') + parseInt('2'))

//com os outros tipos de calculos,não é necessario usar o PARSEINT
console.log( 4 / 4 )
console.log( '4' / '4' )

console.log( 4 * 4 )
console.log( '4' * '4' )

console.log( 4 - 4 )
console.log( '4' - '4' )